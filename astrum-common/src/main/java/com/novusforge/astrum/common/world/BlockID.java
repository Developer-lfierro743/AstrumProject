package com.novusforge.astrum.common.world;

/**
 * Registry of block IDs using scientific names according to "The Formula".
 */
public class BlockID {
    public static final byte AIR = 0;
    public static final byte STONE = 1;
    public static final byte DIRT = 2;
    public static final byte GRASS = 3;
    
    // Scientific Ore Names
    public static final byte FERROUS = 10; // Iron
    public static final byte CUPROUS = 11; // Copper
    public static final byte AURUM = 12;   // Gold
    public static final byte ARGENTUM = 13; // Silver
    
    // Special Blocks
    public static final byte GREENSTONE = 20; // Counterpart of redstone
    public static final byte INFERNITE = 21;  // Counterpart of netherite
}
